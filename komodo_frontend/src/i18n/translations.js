const STORAGE_KEY = 'komodo_lang'

export function getStoredLocale() {
  try {
    const stored = localStorage.getItem(STORAGE_KEY)
    if (stored === 'es' || stored === 'en') return stored
  } catch (_) {}
  return 'en'
}

export function setStoredLocale(locale) {
  try {
    localStorage.setItem(STORAGE_KEY, locale)
  } catch (_) {}
}

/**
 * Obtiene el texto traducido. key en formato 'section.key' (ej: 'sidebar.dashboard').
 */
export function getTranslation(locale, key) {
  const keys = key.split('.')
  let value = translations[locale] || translations.en
  for (const k of keys) {
    value = value?.[k]
    if (value == null) break
  }
  return value != null ? value : (translations.en && getTranslationFallback('en', key)) || key
}

function getTranslationFallback(locale, key) {
  const keys = key.split('.')
  let value = translations[locale]
  for (const k of keys) {
    value = value?.[k]
    if (value == null) return null
  }
  return value
}
